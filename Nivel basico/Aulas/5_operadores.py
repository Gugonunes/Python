"""
Operadores
+, -, *, /, //, **, %, ()
+: adição
-: subtração
*:multiplicação
/:divisão
//:divisão inteira (arredonda o valor)
**:exponencial
%:resto da divisão
(): prioridade
"""
print('adição:', 10 + 10)
print('subtração:', 10 - 2)
print('multiplicação:', 10 * 10)
print('divisão:', 10 / 10)
print('divisao inteira:', 11 // 10)
print('exponencial:', 10 ** 2)
print('resto da divisão:', 10 % 10)
print('prioridade:', (10 * 2) + 2)

